package DAY3;

public class DAY3_Topic1_ConditionalStatements_if_else {
    public static void main(String[] args) {



        int a,b; // declare variable

        a=6; //define variable
        b=3;
//---------------------------------------------------------------------------
        System.out.println("Topic 1");
        //XXXXXXXXX   if Condition    XXXXXXXX
        System.out.println("XXXXXXXXX  if Condition   XXXXXXXXXX");

        if(a>b) {
            System.out.println(" a is more than b ");
        }//if loop end
//---------------------------------------------------------------------------
        System.out.println("-------------------------------------------------------------");
        System.out.println("Topic 2");
        //XXXXXXXX   if else Condition    XXXXXXXX
        System.out.println("XXXXXXXXX  if_else Condition   XXXXXXXXXX");

        if(a<b) {
            System.out.println(" a is more than b ");
        }//if loop end
        else{
            System.out.println(" a is less than b ");
        }


    }//end of main
}//end of public

